<h3 style="color:#080c75"><center><font size="5">Une formation pluridisciplinaire attrayante, diversifiée et inscrite dans les préoccupations régionales et nationales.</h3></center>
<p><font size="4">Ecole Supérieure de Technologie Sidi Bennour : ( EST Sidi Bennour ou ESTS Sidi Bennour ), est un établissement public d’enseignement supérieur à finalité de formation des Techniciens Supérieurs. Elle a été créée en Août 2016 par le Ministère de l’Enseignement Supérieur, de la Formation des Cadres et de la Recherche Scientifique du Royaume du Maroc
L’Ecole Supérieure de Technologie Sidi Bennour (ESTSB) est une composante de l’Université Chouaib Doukkali d’El Jadida. Sa vocation est de former des Techniciens Supérieurs polyvalents, hautement qualifiés et immédiatement opérationnels après leur sortie de l’Ecole en tant que collaborateurs d’ingénieurs et de managers. Sont admis à l’ESTSB, les bacheliers de l’enseignement secondaire technique, scientifique et tertiaire. L’admission à l’Ecole (au de Diplôme Universitaire de Technologie « DUT ») se fait par voie de sélection par ordre de mérite après une présélection sur la base des notes obtenues au baccalauréat. Les candidats doivent être âgés de 22 ans au plus au 31 Décembre de l’année du concours et doivent déposer leurs dossiers de pré-inscription avant le 30 Juin de chaque année universitaire. </font></h6>
<h5 style="color:#617c91">Filières DUT sont ouvertes à l’EST Sidi Bennour :</h5>
<li style="color:#474859"><font size="4">Génie Informatique.</font></li>
<li style="color:#474859"><font size="4">Techniques de Management.</font></li>
<li style="color:#474859"><font size="4">Génie Agronomie.</font></li>
<h5 style="color:#617c91">L’EST Sb continuera sa mission qui consiste à produire davantage de citoyens munis:</h5>
<li style="color:#474859"><font size="4">De solides compétences scientifiques.</font></li>
<li style="color:#474859"><font size="4">D’une large culture générale.</font></li>
<li style="color:#474859"><font size="4">D’une bonne connaissance de leur milieu socioéconomique.</font></li>
<li style="color:#474859"><font size="4">D’une curiosité d’esprit.</font></li>
<li style="color:#474859"><font size="4">Du sens du challenge et de l’ambition.</font></li>
<h5 style="color:#617c91">Orientation appliquée de l’enseignement:</h5>
<p><font size="4">La formation appliquée à l'ESTSB est élargie tournée vers les Technologies d'Information et de Communication, les Techniques de Gestion, les langues vivantes, les Systèmes et Réseaux Informatiques, les conceptions et les réalisations informatiques, c'est pourquoi ses programmes ont toujours le souci de répondre aux exigences et aux attentes de l'environnement socio-économique de l'École et de la police d'appel aux méthodes pédagogiques actives et évoluées. Les
programmes d'enseignement contiennent des Cours magistraux, des Travaux Pratiques (TP), des Travaux Dirigés (TD) et des Dossiers et Travaux de Réalisation, des Projets de Fin d'Etude, des Étapes d'Initiation et des Étapes de Stages Techniques dans les entreprises, les organismes socio-professionnels, les collectivités locales et les administrations publiques.
Durée des études
La durée des études est de deux années universitaires avec 16 modules d’enseignement. La première année s’étale sur 32 semaines suivies de quatre semaines d’étape d’initiation après la fin de S2 et avant le démarrage de S3 dans l’entreprise en juillet ou Aout à raison de 24 jours avec un rapport et une soutenance. La deuxième année est de 36 semaines dont 8 semaines de stage technique et dispensé à raison de 48 jours pour 36 heures par semaine en moyenne avec un rapport et une soutenance.</font></p>
